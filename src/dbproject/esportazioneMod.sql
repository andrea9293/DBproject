  CREATE OR REPLACE TRIGGER "INS_INCONTRI" AFTER INSERT ON PARTITA
BEGIN
IF INSERTING  THEN
INSERT INTO INCONTRI (IDPARTITA, IDSQUADRA)
  SELECT IDPARTITA, IDSQ1
  FROM PARTITA
  WHERE IDPARTITA = (SELECT MAX(IDPARTITA) FROM PARTITA);
END IF;

IF INSERTING THEN
INSERT INTO INCONTRI (IDPARTITA, IDSQUADRA)
  SELECT IDPARTITA, IDSQ2
  FROM PARTITA
  WHERE IDPARTITA = (SELECT MAX(IDPARTITA) FROM PARTITA);
END IF;
END;
/
